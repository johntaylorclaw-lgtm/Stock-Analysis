from __future__ import annotations

from pathlib import Path

from .config import load_schema_registry, load_sources, load_variable_registry
from .paths import DOCS_DIR
from .schema import schema_summary


GENERATED_HEADER = "<!-- GENERATED BY stock-maintain docs-generate; DO NOT EDIT BY HAND. -->"


def markdown_table(headers: list[str], rows: list[list[object]]) -> str:
    out = ["| " + " | ".join(headers) + " |"]
    out.append("| " + " | ".join(["---"] * len(headers)) + " |")
    for row in rows:
        out.append("| " + " | ".join(str(value) for value in row) + " |")
    return "\n".join(out)


def render_schema_dictionary() -> str:
    registry = load_schema_registry()
    lines = [GENERATED_HEADER, "# Schema Dictionary", ""]
    for table in registry.get("tables", []):
        lines.extend(
            [
                f"## {table['name']}",
                "",
                table.get("description", ""),
                "",
                f"- Phase: `{table.get('phase', '')}`",
                f"- Primary key: `{', '.join(table.get('primary_key', []))}`",
                "",
            ]
        )
        rows = []
        for field in table.get("fields", []):
            rows.append(
                [
                    field["name"],
                    field["dtype"],
                    "no" if not field.get("nullable", True) else "yes",
                    field.get("source_api", ""),
                    field.get("source_field", field["name"]),
                    field.get("description", ""),
                ]
            )
        lines.append(markdown_table(["field", "type", "nullable", "source_api", "source_field", "description"], rows))
        lines.append("")
    return "\n".join(lines).rstrip() + "\n"


def render_variable_dictionary() -> str:
    registry = load_variable_registry()
    lines = [GENERATED_HEADER, "# Variable Dictionary", ""]
    variables = registry.get("variables", [])
    rows = []
    for item in variables:
        rows.append(
            [
                item["name"],
                item.get("label_zh", ""),
                item.get("table", ""),
                item.get("module", ""),
                item.get("tier", ""),
                item.get("source_api", item.get("source_type", "")),
                item.get("price_basis", ""),
                item.get("missing_policy", ""),
            ]
        )
    lines.append(
        markdown_table(
            ["name", "label_zh", "table", "module", "tier", "source", "price_basis", "missing_policy"],
            rows,
        )
    )
    return "\n".join(lines).rstrip() + "\n"


def render_source_dictionary() -> str:
    payload = load_sources()
    rows = []
    for api in payload.get("tushare", {}).get("apis", []):
        rows.append([api["name"], api.get("domain", ""), api.get("phase", "")])
    return "\n".join(
        [
            GENERATED_HEADER,
            "# Source API Dictionary",
            "",
            markdown_table(["api", "domain", "phase"], rows),
            "",
        ]
    )


def render_schema_summary() -> str:
    rows = [
        [row["table"], row["phase"], row["fields"], row["primary_key"], row["description"]]
        for row in schema_summary(load_schema_registry())
    ]
    return markdown_table(["table", "phase", "fields", "primary_key", "description"], rows)


def generate_docs() -> list[Path]:
    DOCS_DIR.mkdir(parents=True, exist_ok=True)
    outputs = {
        "generated_schema_dictionary.md": render_schema_dictionary(),
        "generated_variable_dictionary.md": render_variable_dictionary(),
        "generated_source_dictionary.md": render_source_dictionary(),
    }
    paths = []
    for name, content in outputs.items():
        path = DOCS_DIR / name
        path.write_text(content, encoding="utf-8")
        paths.append(path)
    return paths


def check_docs() -> list[str]:
    expected = {
        "generated_schema_dictionary.md": render_schema_dictionary(),
        "generated_variable_dictionary.md": render_variable_dictionary(),
        "generated_source_dictionary.md": render_source_dictionary(),
    }
    diffs = []
    for name, content in expected.items():
        path = DOCS_DIR / name
        if not path.exists():
            diffs.append(f"missing {path}")
            continue
        actual = path.read_text(encoding="utf-8")
        if actual != content:
            diffs.append(f"stale {path}")
    return diffs
