def has_chinese(text: str) -> bool:
    return bool(re.search(r"[\u4e00-\u9fff]", text))


def translate_quarterly(label: str) -> str | None:
    if not label.startswith("Quarterly "):
        return None
    body = label.removeprefix("Quarterly ")
    suffix = ""
    if body.endswith(" YoY"):
        body = body[:-4]
        suffix = "同比"
    elif body.endswith(" QoQ"):
        body = body[:-4]
        suffix = "环比"
    elif body.endswith(" TTM"):
        body = body[:-4]
        suffix = "TTM"
    translated = LABEL_MAP.get(body) or FINANCIAL_LABEL_MAP.get(body)
    if translated:
        return f"单季度{translated}{suffix}"
    return None


def translate_yoy(label: str) -> str | None:
    suffix_map = {" YoY": "同比", " QoQ": "环比"}
    for suffix, zh_suffix in suffix_map.items():
        if label.endswith(suffix):
            body = label[: -len(suffix)]
            translated = LABEL_MAP.get(body) or FINANCIAL_LABEL_MAP.get(body)
            if translated:
                return f"{translated}{zh_suffix}"
    return None


def translate(label: str, table: str, name: str) -> str:
    label = str(label or "").strip()
    if name in FIELD_NAME_LABEL_MAP:
        return FIELD_NAME_LABEL_MAP[name]
    if has_chinese(label):
        return label
    if label in LABEL_MAP:
        return LABEL_MAP[label]
    if label in FINANCIAL_LABEL_MAP:
        return FINANCIAL_LABEL_MAP[label]
    for fn in (translate_quarterly, translate_yoy):
        translated = fn(label)
        if translated:
            return translated
    if name.endswith("_yoy"):
        stem = name[:-4]
        return f"{field_stem_to_zh(stem)}同比"
    if name.endswith("_qoq"):
        stem = name[:-4]
        return f"{field_stem_to_zh(stem)}环比"
    return f"{TABLE_LABELS.get(table, table)}字段：{name}"


